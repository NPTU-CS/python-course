# 數字(整數、小數)
price = 12
discount = 0.85

print(price)
print(discount)

# 字元
apple = 'A'
banana = 'b'

print(apple)
print(banana)

specia1 = '%'

print(specia1)

_newLine = '\n'
sound = '\a'

print(_newLine)
print(sound)

# 字串
LoremIpsum = 'Lorem ipsum dolor sit amet, consectetur adipiscing elit.'
sayHello = 'Hello!\npython is great'

print(LoremIpsum)
print(sayHello)

# 布林值
good = True
bad = False

print(good)
print(bad)
